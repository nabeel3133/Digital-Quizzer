    private Quiz DeserializeData()
    {        
      Quiz e = null;    
        try {
         FileInputStream fileIn = new FileInputStream("SavedSerializedData.ser");
         ObjectInputStream in = new ObjectInputStream(fileIn);
         e = (Quiz)in.readObject();
         in.close();
         fileIn.close();
      }catch(IOException i) {
         i.printStackTrace();
         return null;
      }catch(ClassNotFoundException c) {
         c.printStackTrace();
         return null;
      }
      return e;  
}
            
