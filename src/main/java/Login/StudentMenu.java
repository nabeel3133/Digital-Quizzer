    private void ShowQuizzesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowQuizzesActionPerformed
        QuizzesPanel.setVisible(true);
        int QuizNumber = 1;
        boolean loopRunCheck = false;
        String record = null;
	FileReader in = null;
	try {
	in = new FileReader("Quizzes.txt");
	BufferedReader br = new BufferedReader(in);

	while ((record = br.readLine()) !=null) 
	{            
            loopRunCheck = true;            
            String[] parts = record.split("--------");             
            String Title = parts[0]; 
            String Description = parts[1];
            int QuestionNumber = Integer.parseInt(parts[4]);
             
            if(QuizNumber>0 && QuestionNumber == 1)
            {
                if(QuizNumber == 1)
                {
                Title1.setText(Title);                
                Description1.setText(Description);
                SelectButton1.setVisible(true);    
                }
                else if(QuizNumber == 2)
                {
                Title2.setText(Title);                
                Description2.setText(Description);
                SelectButton2.setVisible(true);    
                }
                else if(QuizNumber == 3)
                {
                Title3.setText(Title);                
                Description3.setText(Description);
                SelectButton3.setVisible(true);    
                }
                else if(QuizNumber == 4)
                {
                Title4.setText(Title);                
                Description4.setText(Description);
                SelectButton4.setVisible(true);    
                }
                else if(QuizNumber == 5)
                {
                Title5.setText(Title);                
                Description5.setText(Description);
                SelectButton5.setVisible(true);    
                }
                else if(QuizNumber == 6)
                {
                Title6.setText(Title);                
                Description6.setText(Description);
                SelectButton6.setVisible(true);    
                }
                else if(QuizNumber == 7)
                {
                Title7.setText(Title);                
                Description7.setText(Description);
                SelectButton7.setVisible(true);    
                }                
                QuizNumber++;                 
            }                        
        }
        br.close();  
        if(loopRunCheck==false)
            {
                String[] options = {"Logout","Exit program"};
                int n = JOptionPane.showOptionDialog(null,"There are no quizzes available at the moment.\nPlease come back later.\n","No quizzes available",JOptionPane.DEFAULT_OPTION,JOptionPane.QUESTION_MESSAGE,null,options,options[0]);
                    if(options[n].contains("Logout"))
                    {
                    this.dispose();                            
                    Login newUser = new Login();
                    newUser.setVisible(true);
                    }
                    else if(options[n].contains("Exit program"))
                    {
                    System.exit(0);        
                    }       
            }
        } 		        
        catch (IOException e) {
	e.getCause();
	}  
       
    }//GEN-LAST:event_ShowQuizzesActionPerformed
    
